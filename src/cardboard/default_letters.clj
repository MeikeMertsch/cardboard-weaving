(ns cardboard.default_letters)

(def char-a
  "00011111111100
00110001100000
00110001100000
00011111111100")

(def lc-a
 "000000000000.0000
  000000.......0000
  00000.......00000
  00000..0.00..0000
  00000..0..0..0000
  00000..00....0000
  000000.000..00000")

(def uc-a
 "00000000001110000
  00000001111110000
  00001111110000000
  00111110110000000
  00110000110000000
  00111110110000000
  00001111110000000
  00000001111110000
  00000000001110000")


(def char-b
  "00011101111000
00110011001100
00110011001100
00111111111100")

(def offset
  "\n00000000000000\n")

(def mapping
  {"a" lc-a "b" char-b "A" uc-a})

(defn pattern-of [string]
  (->> (seq string)
       (map str)
       (map mapping)
       (interpose offset)
       concat
       (apply str)))
